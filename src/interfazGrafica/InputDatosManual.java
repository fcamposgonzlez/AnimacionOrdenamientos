    private void GenerarManualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerarManualActionPerformed
        String datos = DatosManuales.getText();
        ArrayList<String> valores = new ArrayList<>(Arrays.asList(datos.split(",")));
        try {
            new Application(getTipo(),valores,getMetodo(),isDecimal());
        } catch (InterruptedException ex) {
            Logger.getLogger(InputDatosManual.class.getName()).log(Level.SEVERE, null, ex);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(InputDatosManual.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(false);
    }//GEN-LAST:event_GenerarManualActionPerformed

