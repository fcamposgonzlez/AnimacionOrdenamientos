    private void DatosManualesKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_DatosManualesKeyTyped
        char input = evt.getKeyChar();
        if(getSoloInt()){
            if(!(Character.isDigit(input)) && input != ',') {
            evt.consume();    
            }
        }else{
            if((!Character.isDigit(input)) && input != ',' && input != '.') {
            evt.consume();
            }
        }
    }//GEN-LAST:event_DatosManualesKeyTyped

